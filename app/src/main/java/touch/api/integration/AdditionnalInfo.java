package touch.api.integration;

public class AdditionnalInfo {
	private String recipientEmail;
	private String recipientFirstName;
	private String recipeintLastName;
	private String destinataire;
	
	
	public String getRecipientEmail() {
		return recipientEmail;
	}
	public void setRecipientEmail(String recipientEmail) {
		this.recipientEmail = recipientEmail;
	}
	public String getRecipientFirstName() {
		return recipientFirstName;
	}
	public void setRecipientFirstName(String recipientFirstName) {
		this.recipientFirstName = recipientFirstName;
	}
	public String getRecipeintLastName() {
		return recipeintLastName;
	}
	public void setRecipeintLastName(String recipeintLastName) {
		this.recipeintLastName = recipeintLastName;
	}
	public String getDestinataire() {
		return destinataire;
	}
	public void setDestinataire(String destinataire) {
		this.destinataire = destinataire;
	}
	
	
}
