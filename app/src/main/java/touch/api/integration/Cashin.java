package touch.api.integration;

public class Cashin {
	private String serviceId;
	private String recipientPhoneNumber;
	private String partnerId;
	private String partnerTransactionId;
	private String loginApi;
	private String passwordApi;
	private String callBackUrl;
	
	public String getServiceId() {
		return serviceId;
	}
	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}
	public String getRecipientPhoneNumber() {
		return recipientPhoneNumber;
	}
	public void setRecipientPhoneNumber(String recipientPhoneNumber) {
		this.recipientPhoneNumber = recipientPhoneNumber;
	}
	public String getPartnerId() {
		return partnerId;
	}
	public void setPartnerId(String partnerId) {
		this.partnerId = partnerId;
	}
	public String getPartnerTransactionId() {
		return partnerTransactionId;
	}
	public void setPartnerTransactionId(String partnerTransactionId) {
		this.partnerTransactionId = partnerTransactionId;
	}
	public String getLoginApi() {
		return loginApi;
	}
	public void setLoginApi(String loginApi) {
		this.loginApi = loginApi;
	}
	public String getPasswordApi() {
		return passwordApi;
	}
	public void setPasswordApi(String passwordApi) {
		this.passwordApi = passwordApi;
	}
	public String getCallBackUrl() {
		return callBackUrl;
	}
	public void setCallBackUrl(String callBackUrl) {
		this.callBackUrl = callBackUrl;
	}
	

}
